def _get_activation_fn(activation):
    """Return an activation function given a string"""
    #TODO: maybe test with other activation functions?
    if activation == "relu":
        return F.relu
    if activation == "gelu":
        return F.gelu
    if activation == "glu":
        return F.glu
    if activation == "elu":
        return F.elu
    raise RuntimeError(F"activation should be relu/gelu, not {activation}.")